La visión esta focalizada en brindar asistencia tanto legal como en la vida diaria a las personas sordas, puesto que las inquietudes y las dificultades que se presentan en la vida de una persona sorda y su familia son muchas, a pesar de estar en marcha la ley de Igualdad Social, las necesidades son todavía muchas y las barreras con las que estas personas se encuentran a diario las limitan. Es por esto que en la asociación desea mejorar la calidad de vida de las personas sordas tanto como la de sus familiares que también se ven afectados por estas faltas. También es menester de la asociación dictar cursos de lenguaje a señas a estas personas para achicar la brecha en su educación y formación profesional.</p>
