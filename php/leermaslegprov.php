				TITULO I 
Consideraciones Preliminares 

Artículo 1º - La presente Ley tiene por objeto la supresión de las barreras 
comunicacionales existentes entre la comunidad de personas sordas y el resto de 
la sociedad, mediante el reconocimiento de la Lengua de Senas Argentina, y del 
derecho que tienen los sordos a usarla como medio de expresión y comunicación 
válido, en el ámbito del territorio de la Provincia de Mendoza. 

Artículo 2º - A los efectos de la presente Ley, entiéndese como Lengua de Señas 
Argentina (en adelante LSA), al modo de comunicación viso - espacial que 
utilizan las personas sordas que habitan la República Argentina, y que contiene 
diversos regionalismos según la zona en la que es usada. 

Artículo 3º - A los efectos de la presente Ley, el término persona sorda será 
comprensivo de los siguientes conceptos: 

a) Persona Sorda: Aquella que posee una alteración o lesión en la vía auditiva 
que le provoca un impedimento en la audición. 
b) Persona Hipoacúsica: Aquella que posee una alteración o lesión en la vía 
auditiva que le provoca una pérdida auditiva parcial. 

TITULO II 
De la Lengua de Señas Argentina 

Artículo 4º - Reconócese como lengua oficial, en el territorio de la Provincia 
de Mendoza, la LSA con los regionalismos que contiene. 

Artículo 5º - Los órganos legítimos de consulta sobre LSA son las entidades 
reconocidas oficialmente como depositarias de conocimientos y de generación de 
términos y convencionalismos sobre la materia. 

Artículo 6º - La comunidad mendocina de personas sordas, cualquiera sea la forma 
de organización que posea, es una depositaria del conocimiento y responsable de 
la generación de términos y convencionalismos de la LSA. 

Artículo 7º - Todos los habitantes de la Provincia gozan del pleno derecho al 
uso de la LSA como medio de expresión y comunicación válido, y a acceder a una 
educación bilingüe-bicultural: LSA - Lengua Española, en los establecimientos 
educativos para personas sordas de la Provincia de Mendoza. 

Artículo 8º - A partir de la sanción de la presente Ley se incorporarán 
gradualmente en los establecimientos educativos de los distintos niveles y otros 
ámbitos públicos gubernamentales y no gubernamentales, intérpretes de Lengua de 
Señas formados profesionalmente en instituciones oficiales de Nivel Superior. 

Artículo 9º - Hasta la implementación de la formación superior de intérpretes 
profesionales de LSA en la Provincia, el Poder Ejecutivo a través de la 
Subsecretaría de Desarrollo Social, implementará un Registro Temporario de 
Intérpretes de LSA que tendrá las funciones de relevar, evaluar, certificar y 
registrar por única vez, a aquellos intérpretes de LSA que se encuentren 
actuando en el medio al momento de la sanción de la presente. 

Artículo 10 - El Poder Ejecutivo Provincial establecerá el plazo en el que 
expire la posibilidad para ser incorporado al Registro Temporario de 
Intérpretes. No podrán incorporarse más intérpretes al momento de la 
implementación de la Carrera Profesional de Intérpretes de LSA. 

Artículo 11 - Queda facultada la Dirección General de Escuelas para que, en 
coordinación con las instituciones educativas de nivel superior que tengan a su 
cargo la formación profesional de Intérpretes de LSA, cree un sistema especial 
de evaluación para la profesionalización de aquellos que hayan sido incluidos en 
el Registro Temporario de Intérpretes. Este proceso evaluativo contemplará las 
capacidades y la antigüedad en la interpretación de LSA, y sus características 
serán establecidas por medio de la reglamentación de la presente Ley. 

Artículo 12 - La Dirección General de Escuelas impulsará la creación de 
propuestas de formación profesional de intérpretes de LSA en el nivel superior, 
en instituciones de su dependencia y/o jurisdicción nacional. 

Artículo 13 - Cualquier ciudadano tiene derecho a requerir los servicios de un 
intérprete de LSA. 

TITULO III 
De la Comisión de Investigación 

Artículo 14 - Con el objeto de regular el uso de la LSA y garantizar el derecho 
al acceso a la misma de todos los ciudadanos, la Dirección General de Escuelas, 
el Ministerio de Desarrollo Social, en coordinación con las Municipalidades y 
otros organismos oficiales de carácter académico, constituirán una Comisión de 
Investigación de LSA la cual deberá estar integrada por lingüistas, profesores 
de sordos, sociólogos, psicólogos, personas sordas e intérpretes de LSA. 

Artículo 15 - La Comisión de Investigación de LSA será convocada por la 
Subsecretaría de Educación de la Dirección General de Escuelas con no menos de 
sesenta (60) días de antelación al inicio del ciclo lectivo en el que 
desarrollará sus funciones. 

Artículo 16 - La Comisión de Investigación de LSA dará comienzo a sus 
actividades con el inicio del ciclo lectivo siguiente a la sanción de la 
presente ley, la duración de sus funciones por primera y única vez será de un 
(1) año, y tendrá por objetivos, los siguientes: 

a) Implementar mecanismos para realizar un relevamiento de personas sordas en el 
ámbito de la Provincia y prever las formas de actualización permanente del 
mismo. 
b) Garantizar el acceso al conocimiento de la LSA a todos los ciudadanos a 
través de la elaboración de una investigación y la publicación de sus 
conclusiones bajo la forma de un registro gráfico y audiovisual de la totalidad 
de la terminología léxica contenida en la LSA. 
c) Organizar campañas para difundir la LSA en la comunidad a fin de favorecer la 
superación de las barreras comunicacionales de las personas sordas. 
d) Propiciar la participación activa y directa de Asociaciones interesadas en la 
problemática de la comunidad de personas sordas, en las acciones de mejoramiento 
de la calidad de vida de las mismas. 
e) Elaborar a instancias de la Dirección General de Escuelas, una propuesta de 
programa curricular para la implementación en la Provincia de la carrera 
profesional de intérpretes de LSA. 
f) Elaborar nuevos programas de estudio para la organización e implementación de 
la educación bilingüe - bicultural de las personas sordas, habilitando y 
capacitando al personal sordo y oyente que con función docente dictarán los 
contenidos curriculares en LSA. 

Artículo 17 - La Comisión dictará su propio reglamento, sin alejarse de los 
términos y los objetivos enunciados en la presente Ley. 

Artículo 18 - Los miembros de la Comisión duran en sus funciones por el término 
de un (1) año. Artículo 19 - Cumplida la investigación inicial y llegado a su 
término el primer año de funciones, la Comisión podrá convocarse todas las veces 
que fuera necesario en los años subsiguientes. En estos casos las funciones que 
desempeñen sus miembros serán «ad honorem». Para su convocatoria se seguirá el 
mismo procedimiento establecido en el 

Artículo 17, reuniéndose anualmente para evaluar los avances, considerar las 
actualizaciones y modificaciones que surjan del uso de la LSA. 

Artículo 20 - Las erogaciones que solventará el presupuesto asignado a la 
Comisión, comprenden las que surjan en concepto de remuneraciones por los 
servicios prestados, infraestructura de funcionamiento, e impresión y 
publicación de las conclusiones de la investigación. La reglamentación 
establecerá la partida asignada para cumplir con los objetivos. 

TITULO IV 
Consideraciones finales 

Artículo 21 - La Dirección de Comunicaciones del Gobierno de la Provincia de 
Mendoza colaborará con los medios de comunicación, a fin de incorporar en los 
programas televisivos de noticias y de información educativa y cultural, 
intérpretes de LSA, que aseguren el acceso de la persona sorda a la información. 

Artículo 22 - Invítase a la Nación y demás Provincias a adoptar igual medida 
legislativa. 

Artículo 23 - Comuníquese al Poder Ejecutivo. 

DADA EN EL RECINTO DE SESIONES DE LA HONORABLE LEGISLATURA DE LA PROVINCIA DE 
MENDOZA, a los veintinueve días del mes de junio del año dos mil cinco. 


