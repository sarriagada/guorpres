				<h4>Accesibilidad:</h4> 
<p>Parte de la tarea central de esta Comisión Nacional estuvo y está representada por la eliminación de barreras culturales y físicas que se oponen habitualmente a la integración de personas con movilidad y/o comunicación reducida. 
La eliminación de barreras, para ser efectiva, debe ser mancomunada e interprofesional. La supresión de barreras físicas supone operar sobre las valoraciones que la gente hace de las restricciones de movilidad. Téngase en cuenta que el problema no son las restricciones en sí, sino las valoraciones negativas que de ellas se hacen, generándose en consecuencia barreras físicas que segregan, separan y aíslan, desde lo arquitectónico, lo urbanístico, en el transporte y en las comunicaciones.</p>
