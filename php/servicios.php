				<h4>Asesoramiento Integral</h4>
				<p>Sobre diversos temas que interesan a las personas con discapacidad y sus familias, tales como Vivienda, Trabajo, Pensiones No Contributivas, otros planes sociales posibles de tramitar, Medicación, asistencia directa a grupos vulnerables, entre otros. 
En todos estos casos en un primer momento se realiza un abordaje de contexto (registro de la historia social de la persona y su grupo familiar, realidad económica, situación de salud y aspecto habitacional entre otros).   Luego se realiza un diagnostico presuntivo que dará lugar a posibles derivaciones formales, previamente articulados con los distintos programas.  
Trabajo con Provincias, Municipios y Organismos Nacionales: 
La primera articulación se realiza con el Consejo Federal de Discapacidad, consultando al referente de cada provincia o localidad según corresponda. Se toma contacto con las áreas Municipales y provinciales según amerite cada situación particular de intervención. Esta consulta o articulación se lleva a cabo de manera personalizada (entrevista personal), vía telefónica o por nota.</p>
